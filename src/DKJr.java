
public class DKJr {
	
	String state;
	int posX;
	int posY;
	
	DKJr() {
		this.state = "Initial";
		this.posX = 25;
		this.posY = 475;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}
	
	

}
